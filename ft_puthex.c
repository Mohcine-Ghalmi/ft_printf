#include "ft_printf.h"

void	ft_puthex(size_t n)
{
	char hex[] = "0123456789ABCDEF";
	if (n >= 16)
	{
		ft_puthex(n / 16);
		ft_puthex(n % 16);
	}
	else if (n < 16)
		ft_putchar(hex[n]);
}
